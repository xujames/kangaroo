class campaign{
	constructor(campaigns)
	{
		this.campaigns=campaigns
	}
	renderCampaigns()
	{
		this.campaigns.forEach((campaign,index)=>{
			if(campaign.name!=="Referral Program")
			{
			$(".campaigns-option").append(
				$("<div>").append(
					$("<img>").addClass(`campaign${index+1}`),
					$("<h3>").text(campaign.title),
					$("<p>").text(campaign.reward_text)).addClass("swell-campaign-link").attr(
					{"data-campaign-id": campaign.id,"data-display-mode": "modal" }))}
		})
	}
}